const RECENT_KEY = 'spark_ai_emoji_recent_v1'
const MAX_RECENT = 24

/** 模块级 state（供模板响应式消费） */
const _recentRef: { value: string[] } = { value: loadPersist<string[]>(RECENT_KEY, ['👍','❤️','✨','🎉','🔥','😂','🤔','🙏']) }
subscribePersist<string[]>(RECENT_KEY, (v) => {
  if (Array.isArray(v)) _recentRef.value = v
})

export function getRecentEmojis(): string[] {
  return [..._recentRef.value]
}

export function pushRecentEmoji(e: string) {
  if (!e) return
  const next = [e, ..._recentRef.value.filter((x) => x !== e)].slice(0, MAX_RECENT)
  _recentRef.value = next
  savePersist(RECENT_KEY, next)
}

